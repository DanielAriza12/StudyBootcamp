public class CondicionalIf {
    public static void main(String[] args) {
        int numeroIf =10;
        if (numeroIf > 0){
            System.out.println("El número es Positivo");
            }
        else if (numeroIf < 0){
            System.out.println("El número es negativo");
        }
        else if (numeroIf == 0){
            System.out.println("EL numero es igual a 0");
        }
    }
}

